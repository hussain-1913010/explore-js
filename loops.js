// while loop
var i=0;
while(i<10){
    console.log(i);
    i++;
}
// for loop
for(var i=0; i<10; i++){
    console.log(i);
}
// console loop er vitore neyar jonno condition fillup na kora porjonto output e choltise .tobe console.log loop er baire nile ei console ta apna apni output e asbei ....ete loop ghuruk ar na ghuruk..


